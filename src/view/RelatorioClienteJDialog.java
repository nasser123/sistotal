    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Date dataInicial = this.cliente.getDataCadastro();
        Date dataFinal = Datas.getCurrentTime();
        if (jDateChooserDataInicial.getDate() != null) {
            dataInicial = jDateChooserDataInicial.getDate();
        }
        if (jDateChooserDataFinal.getDate() != null) {
            dataFinal = jDateChooserDataFinal.getDate();
        }

        ExecutaRelatorio er = new ExecutaRelatorio();
        
        er.abrirRelatorioListaOS(this.cliente, dataInicial, dataFinal, this.cliente.getTotalAbertoPeriodo(dataInicial, dataFinal));
        
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

