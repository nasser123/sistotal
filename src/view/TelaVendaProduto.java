    private void jButtonInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInserirActionPerformed
        criarVenda();
        SaidaProduto sp = new SaidaProduto();
        sp.setIdvenda(this.venda);
        try {
            sp.setIdproduto(produto);
            sp.setQtd(Integer.parseInt(this.jFormattedTextFieldQtd.getText()));
            sp.setDataSaida(this.jDateChooserDataVenda.getDate());
            sp.setIdtipoSaida(new TipoSaidaController().getTipoSaidaByIdTipoSaida(clienteQuery, SistotalPUEntityManager, 1));
            sp.setValorunitario(BigDecimal.valueOf(Double.parseDouble(this.jTextFieldValorUnitario.getText())));
        } catch (NumberFormatException nfe) {
            nfe.printStackTrace();
        }

        sp.setIdordemServico(null);
        sp.setLucroBruto(produto.getCustoAtual(), sp.getValorunitario(), sp.getQtd());
        this.venda.getSaidaProdutoList().add(sp);

        limpaDadosProduto();
        preencheTotais();

    }//GEN-LAST:event_jButtonInserirActionPerformed

    private Float calculaTotalComDesconto() {
        Float total = 0f;
        for (int i = 0; i < saidaProdutoList.size(); i++) {
            total += saidaProdutoList.get(i).getSubTotal();
        }
        return total;
    }

    private Float calculaTotal() {
        Float total = 0f;
        for (int i = 0; i < saidaProdutoList.size(); i++) {
            total += saidaProdutoList.get(i).getIdproduto().getPrecoVenda1().floatValue() * saidaProdutoList.get(i).getQtd();
        }
        return total;
    }

