    private void preencheDadosProduto(Produto p) {
        this.produto = p;
        this.jTextFieldCodigoBarras.setText(produto.getCodigobarras());
        this.jTextFieldDescricao.setText(produto.getDescricao());
        this.jTextFieldValorUnitario.setText(produto.getPrecoVenda1().toString());

    }

    private void limpaDadosProduto() {
        this.jTextFieldCodigoBarras.setText("");
        this.jTextFieldDescricao.setText("");;
        this.jTextFieldValorUnitario.setText("");
        this.jFormattedTextFieldQtd.setText("1");

        //this.jTextFieldCodigoBarras.requestFocus();

    }

    private void preencheDadosCliente(Cliente c) {
        this.cliente = c;
        this.jTextFieldCliente.setText(this.cliente.getNome());
        this.jTextFieldCodigoCliente.setText(this.cliente.getIdcliente().toString());


    }

    private void preencheTotais() {
        criarVenda();
        this.jLabelPago.setText(this.venda.getTotalPago().toString());
        this.jLabelPendente.setText(this.venda.getPendente().toString());
        this.jLabelTotalComDesc.setText(calculaTotalComDesconto().toString());
        this.jLabelTotal.setText(calculaTotal().toString());

    }

